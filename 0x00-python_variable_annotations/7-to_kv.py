#!/usr/bin/env python3
"""
    Function: to_kv()

"""


def to_kv(k: str, v: int | float) -> tuple:
    """
    Function: to_kv()

    Description:
    Converts a key-value pair into a tuple.

    Parameters:
    - k (str): The key.
    - v (int or float): The value, which can be either an integer or a float.

    Return Type:
    tuple: A tuple containing the key-value pair.
    """

    return (k, v)
